        String studentAdminNumber = textFieldForStudentAdmin.getText();
        
        // give to controller to handle
        boolean result = controller.handleDeleteStudentButtonClicked(studentAdminNumber);
        
        if(result) {
            // reset the textfields
            textFieldForStudentName.setEnabled(true);
            textFieldForStudentAdmin.setEnabled(true);
            textFieldForStudentClass.setEnabled(true);
            
            // reset clickables
            textFieldForStudentName.setText("");
            textFieldForStudentAdmin.setText("");
            textFieldForStudentClass.setText("");
            textFieldForStudentModuleMark.setEnabled(false);
            dropDownModuleCode.setEnabled(false);
            buttonForCreatingStudent.setEnabled(true);
            buttonForDeletingStudent.setEnabled(false);
            buttonForAddingModulesToStudent.setEnabled(false);
            
            // reset the dropdown box for adding modules
            controller.resetDropDownBox(dropDownModuleCode);
        }
