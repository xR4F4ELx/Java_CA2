    private void buttonForCreatingStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonForCreatingStudentActionPerformed
        // TODO add your handling code here:
        // Gather all the Info Here
        String studentName = textFieldForStudentName.getText();
        String studentAdmin = textFieldForStudentAdmin.getText();
        String studentClass = textFieldForStudentClass.getText();
        HashMap<String, String> studentModules = new HashMap<>();
        
        // add student insided the classMap AND set up the dropdown box
        boolean result = controller.handleCreateStudentButtonClicked1(studentName, studentAdmin, studentClass, studentModules);
        
        if(result) {
            // populate the drop down box
            controller.addModulesInsideDropdownbox(studentAdmin, dropDownModuleCode);
            
            // set the textfields to non-editable
            textFieldForStudentName.setEnabled(false);
            textFieldForStudentAdmin.setEnabled(false);
            textFieldForStudentClass.setEnabled(false);

            // disable the create button
            buttonForCreatingStudent.setEnabled(false);
            
            // enable the delete button
            buttonForDeletingStudent.setEnabled(true);
            
            // enable to add modules
            dropDownModuleCode.setEnabled(true);
            textFieldForStudentModuleMark.setEnabled(true);
            buttonForAddingModulesToStudent.setEnabled(true);
        }
        
    }//GEN-LAST:event_buttonForCreatingStudentActionPerformed

